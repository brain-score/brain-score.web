def get_cache_config():
    '''
    Use CACHE_ENV=staging for staging, CACHE_ENV=production for production, CACHE_ENV=development for development.
    '''
    
    env = os.getenv("CACHE_ENV", os.getenv("DJANGO_ENV", "development"))

    # 1) Local dev ‚Üí in-memory
    if env == "development":
        cfg = {
            'default': {
                'BACKEND': 'django.core.cache.backends.locmem.LocMemCache',
                'LOCATION': 'brain-score-default-cache',
                'TIMEOUT': 300,
            }
        }
        print(f"[CACHE] environment={env} ‚Üí using {cfg['default']['BACKEND']} @ {cfg['default']['LOCATION']}")
        return cfg

    # 2) Staging / Production ‚Üí Redis
    if env in ("staging", "production"):
        secret_name = {
            "staging": "brainscore-valkey-staging",
            "production": "brainscore-valkey-production",
        }[env]
        try:
            secrets = get_secret(secret_name, REGION_NAME)
            host = secrets["host"]
            port = secrets.get("port", 6379)
        except Exception as e:
            # couldn‚Äôt load secrets ‚Üí fall back
            print(f"Cache secret load failed ({e}), using locmem fallback")
            return {
                'default': {
                    'BACKEND': 'django.core.cache.backends.locmem.LocMemCache',
                    'LOCATION': 'brain-score-fallback-cache',
                    'TIMEOUT': 300,
                }
            }
        USE_REDIS_TUNNEL = os.getenv("USE_REDIS_TUNNEL") == "true"
        if USE_REDIS_TUNNEL:
            host = "127.0.0.1"
            scheme = "rediss"
        else:
            scheme = "rediss"

        timeout = 7*24*3600 if env == "staging" else 30*24*3600
        prefix = f"brainscore:{env}"
        redis_cfg = {
                'BACKEND': 'django_redis.cache.RedisCache',
                'LOCATION': f"rediss://{host}:{port}/0",
                'TIMEOUT': timeout,
                'OPTIONS': {
                    'CLIENT_CLASS': 'django_redis.client.DefaultClient',
                    'CONNECTION_POOL_KWARGS': {
                        'max_connections': 50,
                        'socket_timeout': 5,
                        'socket_connect_timeout': 5,
                        'retry_on_timeout': True,
                        'health_check_interval': 30,
                    },
                    "SERIALIZER": "django_redis.serializers.pickle.PickleSerializer",
                    'COMPRESSOR': 'django_redis.compressors.zlib.ZlibCompressor',
                    'IGNORE_EXCEPTIONS': True,
                },
                'KEY_PREFIX': prefix,
                'VERSION': 1,
            }
    
        print(f"[CACHE] environment={env} ‚Üí using {redis_cfg['BACKEND']} @ {redis_cfg['LOCATION']}")
        return {'default': redis_cfg, 'redis': redis_cfg}

    # 3) Fallback
    cfg = {
        'default': {
            'BACKEND': 'django.core.cache.backends.locmem.LocMemCache',
            'LOCATION': 'brain-score-fallback-cache',
            'TIMEOUT': 300,
        }
    }
    print(f"[CACHE] environment={env} ‚Üí fallback {cfg['default']['BACKEND']}")
    return cfg

